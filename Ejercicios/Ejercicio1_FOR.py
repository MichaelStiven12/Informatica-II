""" Utilice el ciclo for para recorrer los siguientes iterables, luego imprimalos:
     a) Posiciones = range(10) ,      
     b) Edades = range(10,30,2)
     c) Tamaños = range(1,11, 3)
     d) letras = "ABCDEFGHIJKLMNOPQRSTUVWXYZ"
     e) Estudiantes = ["Cristian", "José", "María", "Juanita", "Marly"]
     f) Calificaciones = (5, 5, 5, 2, 4) """

Posiciones = range(10)
Edades = range(10,30,2)
Tamaños = range(1,11, 3)
letras = "ABCDEFGHIJKLMNOPQRSTUVWXYZ"
Estudiantes = ["Cristian", "José", "María", "Juanita", "Marly"]
Calificaciones = (5, 5, 5, 2, 4)

for i in Posiciones:
    print(i, end = " ")

print("\n")

for i in Edades:
    print(i, end = " ")

print("\n")

for i in Tamaños:
    print(i, end = " ")

print("\n")

for i in letras:
    print(i, end = " ")

print("\n")

for i in Estudiantes:
    print(i, end = " ")

print("\n")

for i in Calificaciones:
    print(i, end = " ")

